GOAL_MULTIPLIERS = {
    "cutting": {
        "calories": 24,    # kcal / kg bodyweight / day
        "protein":  2.2,   # g / kg / day  (high — preserve muscle)
        "carbs":    2.0,   # g / kg / day
        "fat":      0.7,   # g / kg / day
        "meals":    3,
    },
    "lean_bulk": {
        "calories": 30,
        "protein":  2.0,
        "carbs":    3.0,
        "fat":      0.9,
        "meals":    3,
    },
    "bulking": {
        "calories": 36,
        "protein":  1.8,
        "carbs":    4.5,
        "fat":      1.1,
        "meals":    4,
    },
}


def compute_nutrition_targets(weight_kg: float, goal: str) -> dict:
    """Return per-meal macro targets based on bodyweight + fitness goal."""
    goal = goal.lower().replace(" ", "_")
    if goal not in GOAL_MULTIPLIERS:
        raise ValueError(f"Goal must be one of: {list(GOAL_MULTIPLIERS.keys())}")
    m = GOAL_MULTIPLIERS[goal]
    n = m["meals"]
    return {
        "goal":     goal,
        "calories": round(weight_kg * m["calories"] / n, 1),
        "protein":  round(weight_kg * m["protein"]  / n, 1),
        "carbs":    round(weight_kg * m["carbs"]    / n, 1),
        "fat":      round(weight_kg * m["fat"]      / n, 1),
    }


def load_meals(csv_path: str = "cloud_kitchen_meals.csv") -> pd.DataFrame:
    """Load and validate the cloud kitchen meal dataset."""
    if not os.path.exists(csv_path):
        raise FileNotFoundError(f"Dataset not found: '{csv_path}'. Please upload it.")

    df = pd.read_csv(csv_path)

    required = {"food_id", "food_name", "category", "calories", "protein", "carbs", "fat"}
    missing  = required - set(df.columns)
    if missing:
        raise ValueError(f"Dataset missing columns: {missing}")

    df = (df.dropna(subset=list(required))
           .query("calories > 0 and protein >= 0 and carbs >= 0 and fat >= 0")
           .drop_duplicates(subset=["food_name"])
           .reset_index(drop=True))

    print(f"[data_pipeline] ✅ Loaded {len(df)} meals across "
          f"{df['category'].nunique()} categories.")
    return df


