FEATURE_COLS = [
    "calorie_diff", "protein_diff", "carb_diff", "fat_diff",
    "protein_ratio", "carb_ratio",  "fat_ratio",
    "goal_cutting",  "goal_bulking", "goal_lean_bulk",
]


def build_features(df_meals: pd.DataFrame, targets: dict) -> pd.DataFrame:
    """Build 10 ML features for every meal relative to user's nutrition targets."""
    df   = df_meals.copy()
    goal = targets["goal"]

    t_cal  = targets["calories"]
    t_pro  = targets["protein"]
    t_carb = targets["carbs"]
    t_fat  = targets["fat"]

    # Difference features — how far each meal is from the user's target
    df["calorie_diff"] = df["calories"] - t_cal
    df["protein_diff"] = df["protein"]  - t_pro
    df["carb_diff"]    = df["carbs"]    - t_carb
    df["fat_diff"]     = df["fat"]      - t_fat

    # Ratio features — proportional closeness (capped at 2)
    df["protein_ratio"] = (df["protein"] / t_pro ).clip(0, 2)
    df["carb_ratio"]    = (df["carbs"]   / t_carb).clip(0, 2)
    df["fat_ratio"]     = (df["fat"]     / t_fat ).clip(0, 2)

    # Goal one-hot encoding
    df["goal_cutting"]   = int(goal == "cutting")
    df["goal_bulking"]   = int(goal == "bulking")
    df["goal_lean_bulk"] = int(goal == "lean_bulk")

    return df


def _label_meal(row: pd.Series, goal: str) -> int:
    """
    Rule-based label — simulates a nutritionist's judgment.
    Returns 1 (good match) or 0 (poor match).
    """
    if goal == "cutting":
        # Moderate calories, high protein, controlled fat
        return int(
            -250 <= row["calorie_diff"] <= 150
            and row["protein_diff"] >= -15
            and row["fat_diff"]     <= 20
        )
    elif goal == "bulking":
        # At or above caloric target, good protein and carbs
        return int(
            row["calorie_diff"] >= -200
            and row["protein_diff"] >= -20
            and row["carb_diff"]    >= -25
        )
    elif goal == "lean_bulk":
        # Balanced: moderate calories, decent protein, controlled carbs
        return int(
            abs(row["calorie_diff"]) <= 250
            and row["protein_diff"]  >= -20
            and abs(row["carb_diff"]) <= 40
        )
    return 0


def generate_training_data(df_meals: pd.DataFrame,
                            weight_samples: list = None) -> pd.DataFrame:
    """
    Simulate multiple users (50–100 kg) across all 3 goals.
    Each (meal × user_profile) pair becomes one labeled training row.
    """
    if weight_samples is None:
        weight_samples = list(range(50, 105, 5))   # 11 weight points

    all_rows = []
    for goal in GOAL_MULTIPLIERS:
        for weight in weight_samples:
            targets  = compute_nutrition_targets(weight, goal)
            df_feat  = build_features(df_meals, targets)
            df_feat["label"] = df_feat.apply(lambda r: _label_meal(r, goal), axis=1)
            keep = ["food_id", "food_name", "category"] + FEATURE_COLS + ["label"]
            all_rows.append(df_feat[keep])

    df_train = pd.concat(all_rows, ignore_index=True)
    pos = df_train["label"].sum()
    print(f"[feature_engineering] ✅ Training rows : {len(df_train)}")
    print(f"                       Positive (match): {pos}  |  Negative: {len(df_train) - pos}")
    return df_train


